public class DataTypesExample {
    public static void main(String[] args) {
        byte age = 25; // Stores a small integer
        int salary = 50000; // Stores a larger integer
        float pi = 3.14f; // Note the 'f' to specify a float
        double e = 2.718281828459045; // Higher precision
        char grade = 'A'; // Single character
        boolean isJavaFun = true; // Boolean value

        System.out.println("Age: " + age);
        System.out.println("Salary: " + salary);
        System.out.println("Pi: " + pi);
        System.out.println("Euler's Number: " + e);
        System.out.println("Grade: " + grade);
        System.out.println("Is Java Fun? " + isJavaFun);
    }
}